# CONSTANTES
PI = 3.1415926535897931

# ENTRADA
radio = int(input("Ingrese un valor de radio: "))

# PROCESAMIENTO
perimetro = 2 * PI * radio
area = PI * radio ** 2

# SALIDAS
print("El valor del perimetro con radio", radio, "es:", perimetro)
print("El valor del area con radio", radio, "es:", area)
