# ENTRADAS
# Solicita un numero
numero = int(input("Ingrese un numero: "))

# PROCESAMIENTO
# Determina si el numero ingresado es par
if (numero % 2) == 0:
    # Informa que el numero es par
    # SALIDA
    print("El numero", numero, "es par")
# Caso para el numero impar
else:
    # Informa que el numero es impar
    # SALIDA
    print("El numero", numero, "es impar")
